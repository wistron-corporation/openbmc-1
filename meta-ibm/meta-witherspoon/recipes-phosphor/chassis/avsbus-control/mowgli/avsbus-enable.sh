#!/bin/sh

i2cset -y 4 0x30 0x00 0x00 b    # VDD 0  - PAGE set
i2cset -y 4 0x30 0x01 0xB0 b    # VDD 0
i2cset -y 4 0x30 0x00 0x01 b    # VCS 0  - PAGE set
i2cset -y 4 0x30 0x01 0xB0 b    # VCS 0
i2cset -y 4 0x30 0x00 0x00 b    # VCS 0  - PAGE reset
i2cset -y 4 0x31 0x00 0x00 b    # VDN 0  - PAGE set
i2cset -y 4 0x31 0x01 0xB0 b    # VDN 0
